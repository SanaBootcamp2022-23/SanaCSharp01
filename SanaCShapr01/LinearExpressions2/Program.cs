a = double.Parse(Console.ReadLine());
Console.Write("Input b: ");
b = double.Parse(Console.ReadLine());
double y = 2.4 * Abs((Pow(x, 2) + b) / a) + (a - b) * Pow(Sin(a - b), 2) + Pow(10, -2) * (x - b);
